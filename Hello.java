
class Hello {
    public static void main(String[] args) {
        System.out.println("Hello World!");

        // Let's learn variables
        int num = 45;
        char ch = 'a';
        boolean b = true;
        float f = 5.6f;
        long l = 100L;
        short sh = 10;

        // Printing all variables at once
        System.out.println("num: " + num + ", ch: " + ch + ", b: " + b +
                           ", f: " + f + ", l: " + l + ", sh: " + sh);
    }
}
